function checkdata() {
	var login_ID = document.all("loginId");// 得到用户名
	var Login_Password = document.all("loginPassword");// 得到用户密码
	if (login_ID.value.length < 1) {
		alert("用户名不能为空！");
		login_ID.focus();
		return false;
	}

	if (Login_Password.value.length < 1) {
		alert("密码不能为空！");
		Login_Password.focus();
		return false;
	}
}